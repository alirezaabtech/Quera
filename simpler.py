n1 = float(input())
n2 = float(input())
n3 = float(input())
n4 = float(input())

print("Sum :",format(n1+n2+n3+n4, '.6f'))               #"{:.6f}".format(n1+n2+n3+n4)
print("Average :",format((n1+n2+n3+n4)/4, '.6f'))
print("Product :",format(n1*n2*n3*n4, '.6f'))
print("MAX :",format(max(n1,n2,n3,n4), '.6f'))
print("MIN :",format(min(n1,n2,n3,n4), '.6f'))
